export function returnGridImages(data) {
  const list1 = [];
  const list2 = [];
  const list3 = [];
  const list4 = [];
  data.forEach((image, i) => {
    const remainder = (i + 1) % 4;
    let temp;
    switch (remainder) {
      case 0:
        temp = list4;
        break;
      case 1:
        temp = list1;
        break;
      case 2:
        temp = list2;
        break;
      case 3:
        temp = list3;
        break;
      default:
        break;
    }
    temp.push(image);
  });
  return { list1, list2, list3, list4 };
}
